# 1. W pliku functions utwórz funkcję, która przekonwertuje stopnie kelvina w wyłapanej pogodzie na stopnie Celsjusza

def kelvin_to_celsius(kelvin):
    return kelvin - 273.15

# 2. W pliku functions utwórz funkcję, która przekonwertuje metry na sekundę na kilometry na godzine (wiatr)
def ms_to_kmh(wind_speed_ms):
    return wind_speed_ms * 3.6